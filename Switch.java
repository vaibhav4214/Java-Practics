public class Switch {

    public static void main(String[] args) {
        
        int age=100;
        switch (age) {
            case 10:
                System.out.println("age is 10");
                break;
                case 30:
                System.out.println("age is 30");
                break;
                case 20:
                System.out.println("age is 20");
                break;
                
            default:
                System.out.println("Error");;
        }
    }
    
}
